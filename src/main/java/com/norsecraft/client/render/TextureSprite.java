package com.norsecraft.client.render;

public class TextureSprite {

    public final int x;
    public final int y;
    public final int height;
    public final int width;

    public TextureSprite(int x, int y, int height, int width) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }

}
